					<h4 class="text-block__heading lh-title">Conclusions</h4>
					<p class="section-text text-muted text-block__text text-justify">
						Probate is usually necessary in Wyoming to transfer assets held by a decedent in his or her sole name at death worth more than $200,000. If a full probate is needed, the person designated as PR (or who may be appointed PR in the absence of a designation), may expect to go through the following steps:
					</p>

					<ul class="text-muted text-block__list">
						<li>Filing the will with the court and notifying the named PR and readily ascer¬tainable distributees.</li>
						<li>Petitioning the probate court for appointment as PR.</li>
						<li>Filing Petition for Probate with certified copy of death certificate and required information.</li>
						<li>Proving validity of will if necessary.</li>
						<li>Litigating items in will that are contested, if necessary.</li>
						<li>Filing oath of PR.</li>
						<li>Receiving Letters Testamentary from the court clerk.</li>
						<li>Mailing and publishing required notices.</li>
						<li>Accepting or rejecting creditor claims.</li>
						<li>Filing inventory and appraisal with probate court.</li>
						<li>Making required tax filings.</li>
						<li>Filing Interim Reports and Accountings if necessary.</li>
						<li>Filing Final Report, Accounting, and Petition for Distribution.</li>
						<li>Filing Petition for Final Discharge. Probate can be a difficult and complex process but is often necessary to effectively transfer title to a de¬ceased person's Wyoming property.</li>
